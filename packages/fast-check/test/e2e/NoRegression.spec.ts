const testFunc = (value: unknown) => {
  const repr = fc
    .stringify(value)
    .replace(/^(|Big)(Int|Uint|Float)(8|16|32|64)(|Clamped)Array\.from\((.*)\)$/, '$5')
    .replace(/__proto__:null,/g, '')
    .replace(/__proto__:null/g, '');
  for (let idx = 1; idx < repr.length; ++idx) {
    if (repr[idx - 1] === repr[idx] && repr[idx] !== '"' && repr[idx] !== '}') {
      return false;
    }
  }
  return true;
};

