/** @internal */
const base64Alphabet = 'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789+/';

/** @internal */
function decodeBase64Ranges(encoded: string): GraphemeRange[] {
  const ranges: GraphemeRange[] = [];
  // Decode base64 to array of 6-bit values
  const bytes: number[] = [];
  for (let i = 0; i < encoded.length; i++) {
    const c = encoded[i];
    if (c === '=') break;
    const val = base64Alphabet.indexOf(c);
    if (val === -1) continue;
    bytes.push(val);
  }
  // Convert 6-bit values to 8-bit bytes
  const raw: number[] = [];
  for (let i = 0; i < bytes.length; i += 4) {
    raw.push(((bytes[i] << 2) | (bytes[i + 1] >> 4)) & 0xff);
    if (i + 2 < bytes.length) raw.push(((bytes[i + 1] << 4) | (bytes[i + 2] >> 2)) & 0xff);
    if (i + 3 < bytes.length) raw.push(((bytes[i + 2] << 6) | bytes[i + 3]) & 0xff);
  }
  // Each range is 6 bytes: 3 for start, 3 for end
  for (let i = 0; i + 5 < raw.length; i += 6) {
    const start = (raw[i] << 16) | (raw[i + 1] << 8) | raw[i + 2];
    const end = (raw[i + 3] << 16) | (raw[i + 4] << 8) | raw[i + 5];
    ranges.push(start === end ? [start] : [start, end]);
  }
  return ranges;
}

/** @internal */
function lazy(encoded: string): () => GraphemeRange[] {
  let cached: GraphemeRange[] | undefined;
  return () => {
    if (cached === undefined) {
      cached = decodeBase64Ranges(encoded);
    }
    return cached;
  };
}

